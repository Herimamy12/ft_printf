#include "ft_header.h"

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		ft_putchar(str[i++]);
}

int	main(void)
{
	char	c = 'b';

	ft_putchar (c);
	ft_putstr ("salut");
	return (0);
}
